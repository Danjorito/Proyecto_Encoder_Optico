  Apply_LED();

  int step = 1;
  int Red = 255;
  int Green = 255;
  int Blue = 255;
  int defa = 0;
  int Cont_color = 0;
  int EncoderValP = (Get_Encoder_Val())*step; //Previous value for encoder
